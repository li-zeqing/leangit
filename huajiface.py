#绘制滑稽笑脸
import turtle as t
t.setup(800,800)
t.speed(10)
#画脸
t.color("yellow")
t.begin_fill()
t.circle(200)
t.end_fill()

#画嘴
t.left(90)
t.fd(15)
t.right(90)
t.color("yellow")
t.circle(175,270)
t.color("black")
t.circle(175,180)
t.color("yellow")
t.penup()
t.circle(175,270)
t.pendown()

t.color("yellow")
t.goto(-179,258)    #左白眼
t.color("black")
t.fillcolor("white")
t.begin_fill()
t.circle(20,25)
t.circle(-170,50)
t.circle(13,175)
t.circle(150,38)
t.circle(280,13)
t.end_fill()
t.goto(-179,258)
t.seth(0)
t.color("black")
t.begin_fill()
t.circle(16)
t.end_fill()

t.color("yellow")
t.goto(26,258)
t.color("black")
t.fillcolor("white")    #右白眼
t.begin_fill()
t.circle(20,25)
t.circle(-170,50)
t.circle(13,175)
t.circle(150,38)
t.circle(280,13)
t.end_fill()
t.goto(26,258)  #右黑眼坐标
t.seth(0)
t.fillcolor("black")
t.begin_fill()
t.circle(16)
t.end_fill()
#右眼眉毛
t.pencolor("black")
t.penup()
t.goto(60,338)  #右眼眉毛尖坐标
t.pendown()
t.fillcolor("black")
t.begin_fill()
t.seth(25)
t.circle(-90,34)
t.seth(-25)
t.circle(-150,30)
t.left(155)
t.circle(80,55)
t.circle(60,80)
t.end_fill()
#左眼眉毛
t.pencolor("black")
t.penup()
t.goto(-53,338)
t.pendown()
t.fillcolor("black")
t.begin_fill()
t.seth(155)
t.circle(90,34)
t.seth(205)
t.circle(150,30)
t.left(-155)
t.circle(-80,55)
t.circle(-60,50)
t.end_fill()
t.penup()
t.color("yellow")
t.fd(10)
