#画粉色脸蛋
#左脸蛋
t.penup()
t.pencolor("yellow")
t.goto(-160,210)
t.down()
t.seth(90)
t.fillcolor("light pink")
t.begin_fill()
t.circle(-38)
t.end_fill()

#右脸蛋
t.penup()
t.pencolor("yellow")
t.goto(90,210)
t.down()
t.seth(90)
t.fillcolor("light pink")
t.begin_fill()
t.circle(-38)
t.end_fill()

t.done()
